package com.crm.cardlinkmerchant.model;

public class Outlet {
    private String id;
    private String name;
    public Outlet(){}

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }
}
